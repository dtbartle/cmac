func TestAESCMAC(t *testing.T) {
	for i, tc := range testaescmac {
		m := New(tc.key)
		m.Write(tc.msg)
		mac := m.Sum(nil)

		if !bytes.Equal(mac, tc.mac) {
			t.Errorf("tc[%d]: expected %x got %x\n", i, tc.mac, mac)
			t.Errorf("%+v\n", m)
		}
	}
}
